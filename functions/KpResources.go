func getDataValue(translation Translation, namespace string, dataName string) (string, bool) {
	for _, table := range translation.StringTables {
		if table.Namespace == namespace {
			for _, data := range table.DataList {
				//fmt.Println(data.Name)
				if data.Name == dataName {
					return data.Value, true
				}
			}
		}
	}
	return "", false
}
